//5）master公式
//a.所有子问题 ! 规模相同 ! 的递归才能用master公式，T(n) = a * T(n / b) + O(n ^ c)，a、b、c都是常数
//b.如果log(b, a) < c，复杂度为：O(n ^ c)
//    c.如果log(b, a) > c，复杂度为：O(n ^ log(b, a))
//    d.如果log(b, a) == c，复杂度为：O(n ^ c * logn)
//    6）一个补充
